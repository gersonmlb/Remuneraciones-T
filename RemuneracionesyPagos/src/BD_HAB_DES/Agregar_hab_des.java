    private TableRowSorter trsFiltro;
    public void filtro() {
        
        int columnaABuscar = 0;
        if (combofiltro.getSelectedItem() == "DNI") {
            columnaABuscar = 0;
        }
        if (combofiltro.getSelectedItem().toString() == "TIPO") {
            columnaABuscar = 3;
        }
        if (combofiltro.getSelectedItem() == "NOMBRE") {
            columnaABuscar = 5;
        }
        trsFiltro.setRowFilter(RowFilter.regexFilter(jTextFieldParametroBusqueda.getText(), columnaABuscar));
    }
